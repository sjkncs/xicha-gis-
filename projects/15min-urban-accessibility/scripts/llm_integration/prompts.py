def build_prompt(template_key: str, **kwargs) -> str:
    """使用模板构建提示词"""
    template = USER_PROMPTS.get(template_key, "")
    try:
        return template.format(**kwargs)
    except KeyError as e:
        return template + f"\n\n附加信息：{kwargs}"


def build_stats_table(results: dict) -> str:
    """构建统计结果表格"""
    lines = []
    for name, stats in results.items():
        mi = stats.get('morans_i', 'N/A')
        p = stats.get('p_value', 'N/A')
        sig = "显著" if isinstance(p, float) and p < 0.05 else "不显著"
        lines.append(f"| {name} | {mi} | {p} | {sig} |")
    return "\n".join(lines)
